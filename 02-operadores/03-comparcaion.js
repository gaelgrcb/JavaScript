let a = 10;

console.log(a > 5); //muestra en pantalla TRUE que 10 es mayor a 5
console.log(a <= 5); //muestra en pantalla false que 10 es menor o igual a 5
console.log(a < 5); //muestra en pantalla FALSE que 10 menor a 5
console.log(a >= 5); //muestra en pantalla TRUE que 10 es mayor o igual a 5

console.log(a == 10); //muestra en pantalla TRUE que 10 es igual a 10
console.log(a != 10); //muestra en pantalla FALSE que 10 es diferente de 10
