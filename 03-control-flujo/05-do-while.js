// Ejemplo de bucle do-while en JavaScript

// Inicializamos una variable contador
let contador = 1;

do {
    // Dentro del bucle, imprimimos el valor del contador
    console.log("El contador es: " + contador);
    
    // Incrementamos el valor del contador
    contador++;
} while (contador <= 5); // El bucle continÃºa mientras el contador sea menor o igual a 5

// Salida esperada:
// El contador es: 1
// El contador es: 2
// El contador es: 3
// El contador es: 4
// El contador es: 5
